package auction.ui.entity;

public class AuctionStatus {


    private int id;


    private String name;

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getStatusName() {
        return name;
    }
}
